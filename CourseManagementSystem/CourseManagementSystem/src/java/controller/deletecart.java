    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        PrintWriter out = response.getWriter();
        HttpSession session = request.getSession();
        int sessionTimeout = 1800;
        session.setMaxInactiveInterval(sessionTimeout);
        int cartid = Integer.parseInt(request.getParameter("cartid"));
        int userid = Integer.parseInt(request.getParameter("userid"));
        orderDAO od = new orderDAO();
        String ms = od.deleteCart(cartid);
        int total = 0;
        List<cart> list = od.getCartByUserId(userid);
        for ( int i = 0 ; i < list.size() ; i++ ){
            total += list.get(i).getCourseid().getPrice();
        }
        session.setAttribute("mess", ms);
        session.setAttribute("total", total);
        session.setAttribute("listcart", list);
        response.sendRedirect("student.viewcart.jsp");
    } 

